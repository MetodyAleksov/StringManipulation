using System;

namespace _04.CaesarCipher
{
    class Program
    {
        static void Main(string[] args)
        {
            char[] input = Console.ReadLine()
                .ToCharArray();

            for (int i = 0; i < input.Length; i++)
            {
                input[i] = input[i] + 3;
            }
        }
    }
}
